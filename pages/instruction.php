<center><h3>Upgrade Hammers</h3></center>

<br>



The upgrade hammers are used to upgrade your items stats, such as attack of your weapon (melee), defense of your shield, armor of your helmet, armor and legs. bows and crossbows are not upgradeable, except for the perfect shot crossbow (First Tier Daily Raid reward).

<br>

<br>

Each time you use it on an equipment, it has a chance to upgrade your item in +1 stat (armor, attack, defense). If it fails, there is no chance to break your item or to lose any stat you've already got.

<br>

<br>

